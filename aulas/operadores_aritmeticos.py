# os operadores aritmeticos são: + = mais, - = menos, * = multiplicação, / = divisão, ** = potencialização, // = divisão inteira, % = resto da divisão
# se quiser fazer raiz quadrada faça potencialização por meio (1/2)

print(5+2)
print(5-2)
print(5*2)
print(5/2)
print(5**2)
print(5//2)
print(5%2)
print(4**(1/2))
print("------------------------------------------------")
# ordem de precedencia em um código: 1- ()    2- **    3- *, /, //, %    4- +, -

print(5*3+3**2)
print(5*(3+3)**2)